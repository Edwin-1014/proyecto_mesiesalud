     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'codigo' => 'required',
            'nombre_medicamento' => 'required|max:100'         
        ]);

        $farmacos = new Farmacos();
        $farmacos->codigo = $request->codigo;
        $farmacos->nombre_medicamento =$request->nombre_medicamento;
        $farmacos->save();

        return redirect()->route('registroFarmacos.index')->with('success','Codigo-Farmaco Registrado Correctamente');
    }

